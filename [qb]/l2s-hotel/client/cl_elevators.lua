RegisterNetEvent('l2s-hotel:client:ElevatorMenu', function(data)
    local menu = {

    }
    for i=1, #Elevators, 1 do
        if data.index == i then
            menu[#menu+1] = {
                header = 'Current Floor ',
                txt = 'Floor: '..i - 1,
                isMenuHeader = true,
                icon = 'fas fa-circle'
            }
        else
            menu[#menu+1] = {
                header = Elevators[i].text,
                txt = Elevators[i].label,
                icon = 'fas fa-hotel',
                params = {
                    event = "l2s-hotel:client:TakeElevator",
                    args = i
                }
            }
        end
    end

    openMenu(menu)
end)


RegisterNetEvent('l2s-hotel:client:TakeElevator', function(data)
    QBCore.Functions.Progressbar("waiting_elevator", "Waiting for the elevator..", 5000, false, true, {
        disableMovement = true,
        disableCarMovement = true,
        disableMouse = false,
        disableCombat = true,
    }, {}, {}, {}, function() -- Done
        local ped = PlayerPedId()
        DoScreenFadeOut(250)
        while not IsScreenFadedOut() do
            Wait(0)
        end
        FreezeEntityPosition(PlayerPedId(),true)
        UnloadIpl()
        Wait(250)
        local mazendata = data - 1
        for k,v in pairs(stairIpl[mazendata]) do
            -- print("load : "..tostring(v))
            RequestIpl(v)
        end

        TriggerEvent("l2s-hotel:client:FloorSound")
        TriggerEvent('l2s-hotel:client:RefreshEyes:MazenOnToooop', mazendata)

        Wait(250)
        SetEntityCoords(ped, Elevators[data].coords.x, Elevators[data].coords.y, Elevators[data].coords.z, 0, 0, 0, false)
        SetEntityHeading(ped, Elevators[data].coords.w)
        Citizen.Wait(1500)
        DoScreenFadeIn(250)
        FreezeEntityPosition(PlayerPedId(),false)
        Citizen.Wait(5000)
        
    end, function() -- Cancel
        QBCore.Functions.Notify("Canceled..", "error")
    end)
end)

